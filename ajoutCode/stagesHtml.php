            <br>&nbsp;&nbsp;&nbsp;&nbsp;La Caisse d’Assurance Retraite et de la Santé Au Travail (CARSAT) est un organisme de la Sécurité 
            Sociale. Il s’agit d’une structure de droit privé exerçant une mission de service public.
            <br>&nbsp;&nbsp;&nbsp;&nbsp;
            <br>&nbsp;&nbsp;&nbsp;&nbsp;La CARSAT intervient auprès des salariés, des retraités et des entreprises de la région. Elle possède trois missions principales :
            <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- la retraite, que ce soit auprès des salariés qui souhaitent préparer leur retraite ou 	le versement de leurs indemnités aux retraités ;
            <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- l’action sociale, consistant à venir en aide aux personnes fragilisées ou 	handicapées ;
            <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- la gestion des risques professionnels, que ce soit par la prévention auprès des 	entreprises et 	des salariés ou par le versement d’indemnités au titre d’exposition à 	un risque professionnel (exemple : l’amiante).
            <br>&nbsp;&nbsp;&nbsp;&nbsp;
            <br>&nbsp;&nbsp;&nbsp;&nbsp;J’ai évolué au sein du service informatique de la CARSAT, plus particulièrement 
            au pôle Support Développement. Le pôle Support Développement a pour mission d’apporter son aide aux développeurs 
            d’autres CARSAT sur des technologies telles que Microsoft Sharepoint. 
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Ils développent également  des applications basées sur la technologie Sharepoint pour la CARSAT des Pays de la Loire.
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Un service spécifique de la CARSAT, le Service Pilotage de la Performance, a requis le développement d’un petit applicatif afin d’informatiser les demandes qui leur sont faites.
