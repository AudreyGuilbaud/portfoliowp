            <br>&nbsp;&nbsp;&nbsp;&nbsp;Cette petite application Web permet de calculer la moyenne générale
            obtenue au BTS SIO en renseignant chaque note dans chaque matière. Le calcul s'effectue ensuite automatiquement,
            sans validation.
            
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Bien que son utilisation ne soit pas absolument nécessaire, j'ai choisi d'utiliser
            la technologie Ajax à des fins d'entraînement. Le calcul des notes se fait donc en PHP.
            
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Le calcul de la moyenne se fait au fur et à mesure, il est donc possible de ne
            renseigner que quelques notes.
            
